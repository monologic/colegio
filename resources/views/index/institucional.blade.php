La Resolución Sub Directoral Nº 0924-1990 de fecha 28-12-1990 autoriza el funcionamiento que se inicia el 08-04 1991 con 30 estudiantes en el nivel Inicial, 130 en Educación Primaria y 30 en Educación Secundaria. Nace con la conducción de la Asociación Promotora del Colegio ROBERT GAGNE, sin fines de lucro.

En 1997, en uso del D.Leg. 882, “Ley de Promoción de la Inversión Privada en Educación”, con la aprobación de sus promotores, se determina fundar la “Empresa de Servicios Educativos C.E.G.N.E. ROBERT GAGNE Sociedad de Responsabilidad Limitada” para la conducción del Colegio, fecha en la que legalmente asume la razón social de CEGNE ROBERT GAGNE SRL”.

En 2002 se diseña el Proyecto Educativo Institucional, con la participación de Padres de Familia, Estudiantes, Vecinos Notables y los Docentes; instrumento que orienta la Propuesta Educativa propia que se desarrolla.

En 2010 se equipa las aulas con tecnología de la Informática (Proyectores multimedia, cámaras de seguridad, WiFi, uso de laptops, tabletas y celulares por los estudiantes en el desarrollo de clases).

En noviembre de 2013, se firma un Convenio con el Centro de Desarrollo Emprendedor y la Pontificia Universidad Católica del Perú, denominado “Proyecto para el Desarrollo Integral de la Calidad Educativa”. Tiene tres componentes que diferencia el trabajo con PPFF. con Estudiantes y con Docentes. Este convenio está en pleno desarrollo, con un avance muy positivo.

A la fecha la carga docente es 32 en Inicial, 176 en Primaria y 179 en Secundaria. El Colegio ROBERT GAGNE fue la respuesta al verticalismo asfixiante que conducía a la insatisfacción profesional en las escuelas públicas a los docentes fundadores liderados por el Maestro Juan Pedro, quien desde la fundación a la fecha desempeña el cargo de Director.

El ROBERT GAGNE ha logrado ubicarse como referente nacional e internacional a través de la investigación científica como metodología de labor diaria en toda la Educación Básica. Puntualmente participando en las 24 ediciones del evento “FERIA NACIONAL DE CIENCIA Y TECNOLOGÍA” (FENCYT) que ejecuta el CONCYTEC desde hace 24 años (1er. lugar en la Feria de Ciencias e Ingenierías 2013 en Guadalajara- MEXICO y en 2014 representado al Perú en el evento CT+I de Medellín- Colombia obteniendo el 3er. puesto (participaron 11 países) y en el evento MOSTRATEC de Nueva Hamburgo-Brasil el 2do. puesto (participaron 22 paises)</p>
